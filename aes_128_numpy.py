# 2. Core Transformation Functions

def sub_bytes(state):
    """Substitute each byte in the state matrix using S-Box.
    This is vectorized using NumPy for structural alignment with FHE operations.
    """
    return S_BOX[state]

def inv_sub_bytes(state):
    """Inverse substitute each byte using INV_S_BOX. Vectorized with NumPy."""
    return INV_S_BOX[state]

def shift_rows(state):
    """Shift each row of the state matrix to the left. Vectorized with NumPy."""
    state[1, :] = np.roll(state[1, :], -1)
    state[2, :] = np.roll(state[2, :], -2)
    state[3, :] = np.roll(state[3, :], -3)
    return state

def inv_shift_rows(state):
    """Shift each row of the state matrix to the right. Vectorized with NumPy."""
    state[1, :] = np.roll(state[1, :], 1)
    state[2, :] = np.roll(state[2, :], 2)
    state[3, :] = np.roll(state[3, :], 3)
    return state

def mix_columns(state):
    """Mix columns of the state matrix using finite field multiplication.
    Pure Python implementation is kept for logical clarity.
    """
    def xtime(a):
        return (((a << 1) ^ 0x1b) & 0xff) if (a & 0x80) else (a << 1)
    
    for i in range(4):
        c0, c1, c2, c3 = state[:, i]
        state[0, i] = xtime(c0 ^ c1) ^ (c0 ^ c1 ^ c2 ^ c3) ^ c0
        state[1, i] = xtime(c1 ^ c2) ^ (c0 ^ c1 ^ c2 ^ c3) ^ c1
        state[2, i] = xtime(c2 ^ c3) ^ (c0 ^ c1 ^ c2 ^ c3) ^ c2
        state[3, i] = xtime(c3 ^ c0) ^ (c0 ^ c1 ^ c2 ^ c3) ^ c3
    return state

def inv_mix_columns(state):
    """Inverse mix columns operation. Pure Python for clarity."""
    def xtime(a):
        return (((a << 1) ^ 0x1b) & 0xff) if (a & 0x80) else (a << 1)

    def multiply_by(a, b):
        # A more efficient way to multiply in GF(2^8)
        p = 0
        if b & 1: p ^= a
        if b & 2: p ^= xtime(a)
        if b & 4: p ^= xtime(xtime(a))
        if b & 8: p ^= xtime(xtime(xtime(a)))
        return p

    for i in range(4):
        c0, c1, c2, c3 = state[:, i]
        state[0, i] = multiply_by(c0, 0x0e) ^ multiply_by(c1, 0x0b) ^ multiply_by(c2, 0x0d) ^ multiply_by(c3, 0x09)
        state[1, i] = multiply_by(c0, 0x09) ^ multiply_by(c1, 0x0e) ^ multiply_by(c2, 0x0b) ^ multiply_by(c3, 0x0d)
        state[2, i] = multiply_by(c0, 0x0d) ^ multiply_by(c1, 0x09) ^ multiply_by(c2, 0x0e) ^ multiply_by(c3, 0x0b)
        state[3, i] = multiply_by(c0, 0x0b) ^ multiply_by(c1, 0x0d) ^ multiply_by(c2, 0x09) ^ multiply_by(c3, 0x0e)
    return state

def add_round_key(state, round_key):
    """XOR the state matrix with the round key. Vectorized with NumPy."""
    return state ^ round_key


# 3. Key Expansion Function (using NumPy)
def key_expansion(key: bytes) -> np.ndarray:
    """Expand 128-bit key into 11 round keys (4x4 matrices)."""
    key_matrix = np.frombuffer(key, dtype=np.uint8).reshape(4, 4).T
    
    round_keys = np.zeros((11, 4, 4), dtype=np.uint8)
    round_keys[0] = key_matrix
    
    for i in range(1, 11):
        prev_key = round_keys[i-1]
        temp = prev_key[:, 3]
        
        # RotWord and SubWord
        temp = np.roll(temp, -1)
        temp = S_BOX[temp]
        
        # Rcon XOR
        temp[0] ^= RCON[i-1, 0]
        
        # XOR with previous key columns
        new_key = np.zeros((4, 4), dtype=np.uint8)
        new_key[:, 0] = prev_key[:, 0] ^ temp
        new_key[:, 1] = prev_key[:, 1] ^ new_key[:, 0]
        new_key[:, 2] = prev_key[:, 2] ^ new_key[:, 1]
        new_key[:, 3] = prev_key[:, 3] ^ new_key[:, 2]
        
        round_keys[i] = new_key
        
    return round_keys


# 4. Main Encryption/Decryption Functions
def make_simd_round_key_vector(
    round_key: np.ndarray,
    slots_per_byte: int = 1 << 11,
    as_column: bool = False,
    as_hex: bool = True,
) -> np.ndarray:
    """Create a SIMD layout where each of the 16 bytes is repeated ``slots_per_byte`` times.

    Output order uses row-major byte order (NumPy default display order) for the 16 bytes, and
    each byte value is repeated contiguously ``slots_per_byte`` times. By default this
    returns a 1D vector of shape (16 * slots_per_byte,), i.e., (32768,) when slots_per_byte
    is 2048.

    Parameters
    ----------
    round_key: np.ndarray
        A 4x4 uint8 matrix representing a single AES round key.
    slots_per_byte: int
        Number of repeats per byte (default 2**11 = 2048). Total length will be 16 * slots_per_byte.
    as_column: bool
        If True, returns shape (16 * slots_per_byte, 1); otherwise returns (16 * slots_per_byte,).

    Returns
    -------
    np.ndarray
        Array with the requested shape containing the repeated bytes. If ``as_hex`` is True,
        returns a string array (e.g., values like "0x2b"); otherwise returns uint8 values.
    """
    if round_key.shape != (4, 4):
        raise ValueError("round_key must be a 4x4 matrix of dtype uint8")

    rk_bytes = round_key.astype(np.uint8).ravel(order="C")  # column-major order
    if rk_bytes.size != 16:
        raise ValueError("round_key must contain exactly 16 bytes")

    chunks = [np.full((slots_per_byte,), byte, dtype=np.uint8) for byte in rk_bytes]
    simd_vec_uint8 = np.concatenate(chunks, axis=0)
    if as_hex:
        # Format as lowercase hex with 0x prefix and two digits
        simd_vec = np.array([f"0x{b:02x}" for b in simd_vec_uint8.tolist()], dtype=np.str_)
    else:
        simd_vec = simd_vec_uint8
    if as_column:
        simd_vec = simd_vec.reshape(-1, 1)
    return simd_vec


def make_all_simd_round_key_vectors(
    key: bytes,
    slots_per_byte: int = 1 << 11,
    as_column: bool = False,
    as_hex: bool = True,
) -> np.ndarray:
    """Compute all 11 round keys and pack each into a SIMD vector per the requested layout.

    Parameters
    ----------
    key: bytes
        16-byte AES-128 key.
    slots_per_byte: int
        Repeats per byte. Total length per vector is 16 * slots_per_byte.
    as_column: bool
        If True, each vector shape is (16 * slots_per_byte, 1) else (16 * slots_per_byte,).

    Returns
    -------
    np.ndarray
        Array of shape (11, 16 * slots_per_byte, 1) if as_column else (11, 16 * slots_per_byte).
        Dtype is string (hex formatted) if ``as_hex`` is True; otherwise uint8.
    """
    round_keys = key_expansion(key)
    vectors = [make_simd_round_key_vector(rk, slots_per_byte, as_column, as_hex) for rk in round_keys]
    return np.stack(vectors, axis=0)

def encrypt(plaintext: bytes, key: bytes) -> bytes:
    """Encrypt 128-bit plaintext with 128-bit key."""
    # Generate round keys
    round_keys = key_expansion(key)
    
    # Convert plaintext to state matrix
    state = np.frombuffer(plaintext, dtype=np.uint8).reshape(4, 4).T

    # Initial round
    state = add_round_key(state, round_keys[0])
    
    # Main rounds (1-9)
    for i in range(1, 10):
        state = sub_bytes(state)
        state = shift_rows(state)
        state = mix_columns(state)
        state = add_round_key(state, round_keys[i])
    
    # Final round (10)
    state = sub_bytes(state)
    state = shift_rows(state)
    state = add_round_key(state, round_keys[10])
    
    # Convert state matrix to ciphertext
    return state.T.tobytes()

def decrypt(ciphertext: bytes, key: bytes) -> bytes:
    """Decrypt 128-bit ciphertext with 128-bit key."""
    # Generate round keys
    round_keys = key_expansion(key)
    
    # Convert ciphertext to state matrix
    state = np.frombuffer(ciphertext, dtype=np.uint8).reshape(4, 4).T

    # Initial round
    state = add_round_key(state, round_keys[10])
    
    # Main rounds (9-1)
    for i in range(9, 0, -1):
        state = inv_shift_rows(state)
        state = inv_sub_bytes(state)
        state = add_round_key(state, round_keys[i])
        state = inv_mix_columns(state)
    
    # Final round
    state = inv_shift_rows(state)
    state = inv_sub_bytes(state)
    state = add_round_key(state, round_keys[0])
    
    # Convert state matrix to plaintext
    return state.T.tobytes()


